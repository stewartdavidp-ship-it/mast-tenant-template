#!/usr/bin/env node
/*
 * gen-inventory.mjs — generate (or --check) the admin-app inventory.
 *
 * Why: ARCHITECTURE-type docs used to hand-type "13 modules / ~17.6K lines" and
 * rotted to 3-5x wrong as the app grew. This emits those volatile facts straight
 * from the source so the doc can never silently drift again.
 *
 *   node scripts/gen-inventory.mjs           # write docs/generated/admin-inventory.md
 *   node scripts/gen-inventory.mjs --check   # exit 1 if the committed file is stale
 *
 * Output is DETERMINISTIC (no timestamps) so --check is stable in CI.
 */
import { readFileSync, readdirSync, writeFileSync, statSync } from 'node:fs';
import { join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(fileURLToPath(import.meta.url), '..', '..'); // repo root
const OUT = join(ROOT, 'docs', 'generated', 'admin-inventory.md');
const INDEX = join(ROOT, 'app', 'index.html');
const MODULES_DIR = join(ROOT, 'app', 'modules');

const lineCount = (p) => (readFileSync(p, 'utf8').match(/\n/g) || []).length;

// recurse app/modules for *.js (includes workflows/)
function listModuleFiles(dir) {
  const out = [];
  for (const e of readdirSync(dir, { withFileTypes: true })) {
    const p = join(dir, e.name);
    if (e.isDirectory()) out.push(...listModuleFiles(p));
    else if (e.name.endsWith('.js')) out.push(p);
  }
  return out.sort();
}

// parse MODULE_MANIFEST { src -> routes[] } from index.html (best-effort, tolerant)
function parseManifest() {
  const txt = readFileSync(INDEX, 'utf8');
  const start = txt.indexOf('var MODULE_MANIFEST = {');
  const map = {};
  if (start === -1) return map;
  const end = txt.indexOf('\n};', start);
  const block = txt.slice(start, end === -1 ? undefined : end);
  const re = /src:\s*'([^']+)'\s*,\s*routes:\s*\[([^\]]*)\]/g;
  let m;
  while ((m = re.exec(block))) {
    const src = m[1]; // e.g. modules/orders.js
    const routes = m[2].split(',').map((s) => s.trim().replace(/^['"]|['"]$/g, '')).filter(Boolean);
    map[src] = routes;
  }
  return map;
}

function render() {
  const indexLines = lineCount(INDEX);
  const files = listModuleFiles(MODULES_DIR);
  const manifest = parseManifest();
  let totalModuleLines = 0;
  const rows = files.map((f) => {
    const rel = relative(join(ROOT, 'app'), f).split('\\').join('/'); // modules/xyz.js
    const lines = lineCount(f);
    totalModuleLines += lines;
    const routes = manifest[rel] || [];
    return { rel, lines, routes };
  });
  const routed = rows.filter((r) => r.routes.length).length;

  const L = [];
  L.push('<!-- AUTO-GENERATED by scripts/gen-inventory.mjs — DO NOT EDIT.');
  L.push('     Refresh with: node scripts/gen-inventory.mjs  (CI gate: docs-inventory). -->');
  L.push('# Admin app — generated inventory');
  L.push('');
  L.push('Source-of-truth counts for the admin app, generated from the code so docs');
  L.push('cannot silently drift. See `mast-docs/platform/doc-sync-proposal.md`.');
  L.push('');
  L.push(`- **Core shell:** \`app/index.html\` — **${indexLines.toLocaleString('en-US')}** lines`);
  L.push(`- **Feature modules:** **${files.length}** files in \`app/modules/\` (**${routed}** routed), **${totalModuleLines.toLocaleString('en-US')}** lines combined`);
  L.push('');
  L.push('| Module file | Lines | Routes |');
  L.push('|---|---:|---|');
  for (const r of rows) {
    L.push(`| \`${r.rel}\` | ${r.lines.toLocaleString('en-US')} | ${r.routes.length ? r.routes.join(', ') : '—'} |`);
  }
  L.push('');
  return L.join('\n');
}

const content = render();
if (process.argv.includes('--check')) {
  let current = '';
  try { current = readFileSync(OUT, 'utf8'); } catch {}
  if (current !== content) {
    console.error('✗ docs/generated/admin-inventory.md is STALE.');
    console.error('  Run:  node scripts/gen-inventory.mjs   and commit the result.');
    process.exit(1);
  }
  console.log('✓ admin-inventory.md is current.');
} else {
  writeFileSync(OUT, content);
  console.log(`wrote ${relative(ROOT, OUT)}`);
}
